class Solution {
  public:
    Node* segregate(Node* head) {
        // code here
         if(head == NULL )return NULL;
          vector<int>ans;
          Node* temp = head;
        while(temp != NULL){
            ans.push_back(temp->data);
            temp = temp->next;
        }
        sort(ans.begin(),ans.end());
          Node* temp1 = new Node(ans[0]) ;
          Node* temp2 = temp1;
        for(int i =1; i<ans.size();i++){
             temp2->next = new Node(ans[i]);
             temp2 = temp2->next;
        }
        return temp1;
    }
};



